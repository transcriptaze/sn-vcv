#include "TF.hpp"

const double TF::Δf = 512.0;
const double TF::FS = 44100.0;

double TF::interpolate(ANTIALIAS mode, const double f) {
    double f0;
    int ix;
    int jx;

    switch (mode) {
    case NONE:
        return 1.0;

    case ANTIALIAS::X1F1:
        f0 = std::fmod(f, FS);
        ix = std::floor(f0 / Δf);
        jx = std::ceil(f0 / Δf);

        if (ix == jx) {
            return std::get<1>(X1F1[ix]);
        } else {
            const auto u = X1F1[ix];
            const auto v = X1F1[jx];

            const double f1 = std::get<0>(u);
            const double f2 = std::get<0>(v);
            const double p = std::get<1>(u);
            const double q = std::get<1>(v);

            return p + (q - p) * (f0 - f1) / (f2 - f1);
        }

    case ANTIALIAS::X1F2:
        f0 = std::fmod(f, FS);
        ix = std::floor(f0 / Δf);
        jx = std::ceil(f0 / Δf);

        if (ix == jx) {
            const double h = std::get<1>(X1F2[ix]);

            return h * h;
        } else {
            const auto u = X1F2[ix];
            const auto v = X1F2[jx];

            const double f1 = std::get<0>(u);
            const double f2 = std::get<0>(v);
            const double p = std::get<1>(u);
            const double q = std::get<1>(v);
            const double h = p + (q - p) * (f0 - f1) / (f2 - f1);

            return h * h;
        }

    case ANTIALIAS::X2F1:
        f0 = std::fmod(f, 2.0 * FS);
        ix = std::floor(f0 / Δf);
        jx = std::ceil(f0 / Δf);

        if (ix == jx) {
            return std::get<1>(X2F1[ix]);
        } else {
            const auto u = X2F1[ix];
            const auto v = X2F1[jx];

            const double f1 = std::get<0>(u);
            const double f2 = std::get<0>(v);
            const double p = std::get<1>(u);
            const double q = std::get<1>(v);

            return p + (q - p) * (f0 - f1) / (f2 - f1);
        }

    case ANTIALIAS::X2F2:
        f0 = std::fmod(f, 2.0 * FS);
        ix = std::floor(f0 / Δf);
        jx = std::ceil(f0 / Δf);

        if (ix == jx) {
            const double h = std::get<1>(X2F2[ix]);

            return h * h;
        } else {
            const auto u = X2F2[ix];
            const auto v = X2F2[jx];

            const double f1 = std::get<0>(u);
            const double f2 = std::get<0>(v);
            const double p = std::get<1>(u);
            const double q = std::get<1>(v);
            const double h = p + (q - p) * (f0 - f1) / (f2 - f1);

            return h * h;
        }

    case ANTIALIAS::X4F1:
        f0 = std::fmod(f, 4.0 * FS);
        ix = std::floor(f0 / Δf);
        jx = std::ceil(f0 / Δf);

        if (ix == jx) {
            return std::get<1>(X4F1[ix]);
        } else {
            const auto u = X4F1[ix];
            const auto v = X4F1[jx];

            const double f1 = std::get<0>(u);
            const double f2 = std::get<0>(v);
            const double p = std::get<1>(u);
            const double q = std::get<1>(v);
            return p + (q - p) * (f0 - f1) / (f2 - f1);
        }

    case ANTIALIAS::X4F2:
        f0 = std::fmod(f, 4.0 * FS);
        ix = std::floor(f0 / Δf);
        jx = std::ceil(f0 / Δf);

        if (ix == jx) {
            const double h = std::get<1>(X4F2[ix]);

            return h * h;
        } else {
            const auto u = X4F2[ix];
            const auto v = X4F2[jx];

            const double f1 = std::get<0>(u);
            const double f2 = std::get<0>(v);
            const double p = std::get<1>(u);
            const double q = std::get<1>(v);
            const double h = p + (q - p) * (f0 - f1) / (f2 - f1);

            return h * h;
        }

    default:
        return 1.0;
    }
}

